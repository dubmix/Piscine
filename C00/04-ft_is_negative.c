#include <unistd.h>

void	ft_is_negative(int n)
{
	int		i;
	char	c;
	char	d;

	i = n;
	if (i < 0)
	{
		c = 'N';
		write(1, &c, 1);
	}
	else
	{
		d = 'P';
		write(1, &d, 1);
	}
}
